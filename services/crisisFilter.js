// ─── 5. 감지 함수 ────────────────────────────────────────────────────────────
function detectCrisis(text) {
  if (!text || typeof text !== 'string') {
    return { detected: false, score: 0, matched: [] };
  }

  const normalized = text.replace(/\s+/g, '');
  let score = 0;
  const matched = [];

  // 완전일치 키워드 — 하나라도 맞으면 score +2 (중복 가산 방지)
  for (const kw of EXACT_KEYWORDS) {
    if (normalized.includes(kw.replace(/\s+/g, ''))) {
      score += 2;
      matched.push(`kw:${kw}`);
      break;
    }
  }

  // 정규식 패턴 — 각각 가산
  for (const { pattern, weight, name } of REGEX_PATTERNS) {
    if (pattern.test(text)) {
      score += weight;
      matched.push(`re:${name}`);
    }
  }

  // 과장 표현 감쇠
  for (const { pattern, name } of HYPERBOLE_PATTERNS) {
    if (pattern.test(text)) {
      score -= 2;
      matched.push(`hype:${name}`);
    }
  }

  return {
    detected: score >= 2,
    score,
    matched: matched.slice(0, 5),
  };
}

